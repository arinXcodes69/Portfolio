const getDatePartsInTimeZone = (date, timeZone) => {
  const formatter = new Intl.DateTimeFormat("en-CA", {
    timeZone,
    year: "numeric",
    month: "numeric",
    day: "numeric",
  });

  const parts = formatter.formatToParts(date);
  const getPart = (type) =>
    Number(parts.find((part) => part.type === type)?.value || 0);

  return {
    year: getPart("year"),
    month: getPart("month"),
    day: getPart("day"),
  };
};

const getCurrentAge = () => {
  const today = getDatePartsInTimeZone(new Date(), siteData.profile.timezone);
  const { year, month, day } = siteData.profile.birthDate;
  let age = today.year - year;

  const birthdayReached =
    today.month > month || (today.month === month && today.day >= day);

  if (!birthdayReached) {
    age -= 1;
  }

  return age;
};

