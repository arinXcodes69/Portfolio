const setStableTextHeight = (element, variants, renderVariant) => {
  if (!element || !variants.length) {
    return;
  }

  const width = element.getBoundingClientRect().width;

  if (!width) {
    return;
  }

  const clone = element.cloneNode(false);
  clone.style.position = "absolute";
  clone.style.visibility = "hidden";
  clone.style.pointerEvents = "none";
  clone.style.left = "-9999px";
  clone.style.top = "0";
  clone.style.width = `${width}px`;
  clone.style.minHeight = "0";
  clone.style.height = "auto";
  clone.style.maxWidth = "none";
  clone.style.opacity = "1";
  clone.classList.remove("is-swapping");
  document.body.appendChild(clone);

  let maxHeight = 0;

  variants.forEach((variant) => {
    renderVariant(clone, variant);
    maxHeight = Math.max(maxHeight, Math.ceil(clone.getBoundingClientRect().height));
  });

  clone.remove();

  if (maxHeight > 0) {
    element.style.minHeight = `${maxHeight}px`;
  }
};

const swapTextContent = (element, nextValue, renderVariant) => {
  if (!element) {
    return;
  }

  element.classList.add("is-swapping");

  window.setTimeout(() => {
    renderVariant(element, nextValue);
    requestAnimationFrame(() => {
      element.classList.remove("is-swapping");
    });
  }, 140);
};

const hydrateLiveBits = () => {
  const yearSlot = document.querySelector("#year");
  const timeSlot = document.querySelector("#local-time");
  const heroHeading = document.querySelector("#hero-heading");
  const quoteRotator = document.querySelector("#quote-rotator");
  const presencePill = document.querySelector("#presence-pill");
  const availabilityText = document.querySelector("#availability-text");
  const heroPulse = document.querySelector("#hero-pulse");
  const heroSyncPill = document.querySelector("#hero-sync-pill");
  const rotatingHeadlines = [
    [
      "Arin Varconkar.",
      "AI & DS student,",
      "building with clarity.",
    ],
    [
      "Code, design,",
      "and steady learning",
      "in one growing space.",
    ],
    [
      "From C and C++ practice",
      "to Canva-made visuals,",
      "I like clean output.",
    ],
  ];
  const rotatingQuotes = [
    "“The best websites don’t just explain who you are. They make people feel it.”",
    "“A personal website should grow with the person behind it.”",
    "“Learning in public can become part of your identity.”",
  ];
  const rotatingSignals = siteData.heroSignals;
  const stabilizeRotatingAreas = () => {
    setStableTextHeight(heroHeading, rotatingHeadlines, (element, variant) => {
      element.innerHTML = variant.join("<br />");
    });
    setStableTextHeight(quoteRotator, rotatingQuotes, (element, variant) => {
      element.textContent = variant;
    });
    setStableTextHeight(heroPulse, rotatingSignals, (element, variant) => {
      element.textContent = variant;
    });
  };

  if (yearSlot) {
    yearSlot.textContent = new Date().getFullYear();
  }

  stabilizeRotatingAreas();

  const updateTime = () => {
    if (!timeSlot) {
      return;
    }

    const timeContext = getTimeContext();
    const formatter = new Intl.DateTimeFormat("en-IN", {
      hour: "numeric",
      minute: "2-digit",
      second: "2-digit",
      timeZone: siteData.profile.timezone,
    });

    timeSlot.textContent = `Local time: ${formatter.format(new Date())}`;

    if (presencePill) {
      presencePill.textContent = timeContext.label;
    }

    if (availabilityText) {
      availabilityText.textContent = timeContext.mode;
    }

    if (heroSyncPill) {
      heroSyncPill.textContent =
        repoFetchState.status === "loaded"
          ? "GitHub synced"
          : repoFetchState.status === "error"
            ? "Fallback mode"
            : "Updated live";
    }
  };

  updateTime();
  window.clearInterval(timeIntervalId);
  timeIntervalId = window.setInterval(updateTime, 1000);

  window.clearInterval(headlineIntervalId);
  headlineIntervalId = window.setInterval(() => {
    if (!heroHeading || !quoteRotator) {
      return;
    }

    headlineIndex = (headlineIndex + 1) % rotatingHeadlines.length;
    swapTextContent(heroHeading, rotatingHeadlines[headlineIndex], (element, variant) => {
      element.innerHTML = variant.join("<br />");
    });
    swapTextContent(quoteRotator, rotatingQuotes[headlineIndex], (element, variant) => {
      element.textContent = variant;
    });
    swapTextContent(heroPulse, rotatingSignals[headlineIndex], (element, variant) => {
      element.textContent = variant;
    });
  }, 5000);

  window.addEventListener("resize", () => {
    stabilizeRotatingAreas();

    if (window.innerWidth > 640 && navOpen) {
      navOpen = false;
      syncNavState();
    }
  });
};

const setupHeroInteractions = () => {
  document.querySelectorAll(".hero-copy, .hero-card").forEach((panel) => {
    panel.addEventListener("pointermove", (event) => {
      const frameId = heroGlowFrames.get(panel);

      if (frameId) {
        cancelAnimationFrame(frameId);
      }

      const nextFrame = requestAnimationFrame(() => {
        const rect = panel.getBoundingClientRect();
        const x = ((event.clientX - rect.left) / rect.width) * 100;
        const y = ((event.clientY - rect.top) / rect.height) * 100;

        panel.style.setProperty("--pointer-x", `${x}%`);
        panel.style.setProperty("--pointer-y", `${y}%`);
      });

      heroGlowFrames.set(panel, nextFrame);
    });

    panel.addEventListener("pointerleave", () => {
      panel.style.removeProperty("--pointer-x");
      panel.style.removeProperty("--pointer-y");
    });
  });
};

const setupRevealObserver = () => {
  const revealItems = document.querySelectorAll(".reveal:not(.is-visible)");

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.16,
    }
  );

  revealItems.forEach((item) => observer.observe(item));
};

