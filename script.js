const setStableTextHeight = (element, variants, renderVariant) => {
  if (!element || !variants.length) {
    return;
  }

  const width = element.getBoundingClientRect().width;

  if (!width) {
    return;
  }

  const clone = element.cloneNode(false);
  clone.style.position = "absolute";
  clone.style.visibility = "hidden";
  clone.style.pointerEvents = "none";
  clone.style.left = "-9999px";
  clone.style.top = "0";
  clone.style.width = `${width}px`;
  clone.style.minHeight = "0";
  clone.style.height = "auto";
  clone.style.maxWidth = "none";
  clone.style.opacity = "1";
  clone.classList.remove("is-swapping");
  document.body.appendChild(clone);

  let maxHeight = 0;

  variants.forEach((variant) => {
    renderVariant(clone, variant);
    maxHeight = Math.max(maxHeight, Math.ceil(clone.getBoundingClientRect().height));
  });

  clone.remove();

  if (maxHeight > 0) {
    element.style.minHeight = `${maxHeight}px`;
  }
};

const swapTextContent = (element, nextValue, renderVariant) => {
  if (!element) {
    return;
  }

  element.classList.add("is-swapping");

  window.setTimeout(() => {
    renderVariant(element, nextValue);
    requestAnimationFrame(() => {
      element.classList.remove("is-swapping");
    });
  }, 140);
};

